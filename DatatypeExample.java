class DatatypeExample{
	public static void main(String args[]){
		System.out.println("starting of a primitive datatype");
		System.out.println("-----Here it begins----");
		boolean bool = true;
		char stuName = 2;
		short shorty = 21;
		int integer = 23456;
		long longy = 97863761100L;
		float floaty = 23.4567f;
		double doubely = 23.24442346574d;
		System.out.println(bool);
		System.out.println(stuName);
		System.out.println(shorty);
		System.out.println(integer);
		System.out.println(longy);
		System.out.println(floaty);
		System.out.println(doubely);
	}
}
		
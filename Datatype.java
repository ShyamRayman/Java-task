class Datatype{
	public static void main(String args[]){
		boolean bool = true;
		char character2 = '$';
		byte small = 23;
		short mini = 555;
		int number  = 000001;
		long mobileNumber = 93760084723L;
		float decimal = 23.000001F;
		double longDecimal = 23.00001020138137D;
		System.out.println("output of primitive Datatype");
		System.out.println(bool);
		System.out.println(character2);
		System.out.println(small);
		System.out.println(mini);
		System.out.println(number);
		System.out.println(mobileNumber);
		System.out.println(decimal);
		System.out.println(longDecimal);

	String email = "muthukarupan2gmail.com";
	System.out.println("Start of non-primitive datatype");
	System.out.println(email);
}
}

		
